
public class Multidimarr{
    public static void main(String[] args){
            int[][] myNumbers = {{}, {}};
            for (int i=0; i<myNumbers.length; ++i){
                for (int j=0; j<myNumbers[i].length; ++j){
                    System.out.println(myNumbers[i][j]);
                }
            }
        }
    }


